      PROGRAM C350 A
      DP=.1E-7
      PRINT 12,DP
      PRINT 11
C
      A1Z=0.
      AIE1Z=0.
      DO 1 I=1,4
      AIMZ=(I-1)*2.
      REZ=.25
      DO 2 J=1,5
      REZ=2*REZ
      CALL EXPE1Z(REZ,AIMZ,DP,R1Z,A1Z)
      REE1Z=R1Z
      AIE1Z=A1Z
      IF(REZ.LE.2..AND.AIMZ.EQ.0.) GO TO 5
      EX=EXP(REZ)
      SI=SIN(AIMZ)
      CO=COS(AIMZ)
      RE=REZ*R1Z-AIMZ*A1Z
      AI=REZ*A1Z+AIMZ*R1Z
      REE1Z=EX*(CO*RE-SI*AI)
      AIE1Z=EX*(CO*AI+SI*RE)
 5    CONTINUE
      PRINT 10,I,J,REZ,AIMZ,REE1Z,AIE1Z
 2    CONTINUE
 1    CONTINUE
C
 11   FORMAT(2X,'I',2X,'J',3X,'REZ',2X,'AIMZ',10X,'REE1Z',12X,'AIE1Z')
 10   FORMAT(1X,2I3,2F5.2,2(2X,2E18.10))
 12   FORMAT(1X,'DP=',E10.2)
      END
*EXECUTE
