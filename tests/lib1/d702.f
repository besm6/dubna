      PROGRAM D702
      DIMENSION A(384),A1(384),S(2)
      COMPLEX C(192),C1(192)
      EQUIVALENCE (C,S)
      EQUIVALENCE (A,C),(A1,C1)
      DATA N,N1,N2,N3/187,8,12,2/
C
C
C       IN THE CASES WHERE REAL AND IMAG. VALUES ARE HELD IN TWO 1-D
C       ARRAYS, THESE TWO ARRAYS ARE REPRESENTED BY THE ARRAY A(2N).
C       THE FIRST ARRAY IS REPRESENTED BY A(1) TO A(N), AND THE SECOND
C       BY A(N+1) TO A(2N).
C       THE ARRAY A1 IS USED IN THE SAME WAY TO STORE THE TWO SETS OF
C       ORIGINAL VALUES BEFORE FFT IS CALLED, SINCE THE SUBROUTINE
C       ALTERS THE VALUES IN A.
C
C       WHEN THE COMPLEX ARRAY C IS USED INSTEAD OF THE >TWO> REAL
C       ARRAYS, C IS EQUIVALENCED TO A, THUS SAVING SPACE, AND C WILL
C       BE OF LENGTH N.  LIKEWISE, WITH A1 AND C1.
C
C
      PRINT 100
 100  FORMAT(1H1)
      READ 2,A1
      PRINT 1,A1
   1  FORMAT((1X,6E19.10)/)
  2   FORMAT(4E20.10)
      CALL SETRN(A,A1,N)
      CALL CFT(A,A(N+1),N,N,N,1)
      CALL CFT(A,A(N+1),N,N,N,-1)
      PRINT 200
 200  FORMAT(///1H0,10HTEST NO. 1/1H ,10(1H*)/1H0,24HSINGLE VARIATE TRAN
     1SFORM/45H0REAL AND IMAG. VALUES HELD IN TWO 1-D ARRAYS/)
      CALL ERRTST(A,A(N+1),A1,A1(N+1),N)
C
C
      CALL SETRN(A,A1,N)
      CALL CFT(C,S(2),N,N,N,2)
      CALL CFT(C,S(2),N,N,N,-2)
      PRINT 300
 300  FORMAT(/1H0,10HTEST NO. 2/1H ,10(1H*)/1H0,24HSINGLE VARIATE TRANSF
     1ORM/44H0REAL AND IMAG. VALUES HELD IN COMPLEX ARRAY/)
      CALL ERRTST(A,A(N+1),A1,A1(N+1),N)
C
C
C
      N=N1*N2*N3
      CALL SETRN(A,A1,N)
      CALL CFT(A,A(N+1),N,N1,N1,1)
      CALL CFT(A,A(N+1),N,N2,N1*N2,1)
      CALL CFT(A,A(N+1),N,N3,N,1)
      CALL CFT(A,A(N+1),N,N1,N1,-1)
      CALL CFT(A,A(N+1),N,N2,N1*N2,-1)
      CALL CFT(A,A(N+1),N,N3,N,-1)
      PRINT 400
 400  FORMAT(/1H0,10HTEST NO. 3/1H ,10(1H*)/1H0,21HTRI-VARIATE TRANSFORM
     1/45H0REAL AND IMAG. VALUES HELD IN TWO 1-D ARRAYS/)
      CALL ERRTST(A,A(N+1),A1,A1(N+1),N)
C
C
      CALL SETRN(A,A1,N)
      CALL CFT(C,S(2),N,N1,N1,2)
      CALL CFT(C,S(2),N,N2,N1*N2,2)
      CALL CFT(C,S(2),N,N3,N,2)
      CALL CFT(C,S(2),N,N1,N1,-2)
      CALL CFT(C,S(2),N,N2,N1*N2,-2)
      CALL CFT(C,S(2),N,N3,N,-2)
      PRINT 500
 500  FORMAT(1H0,10HTEST NO. 4/1H ,10(1H*)/1H0,21HTRI-VARIATE TRANSFORM/
     1   45H0REAL AND IMAG. VALUES HELD IN COMPLEX ARRAY/)
      CALL ERRTST(A,A(N+1),A1,A1(N+1),N)
C
      PRINT 100
      END
      SUBROUTINE SETRN(A,A1,N)
      DIMENSION A(384),A1(384)
C
C       THIS ROUTINE READS RANDOM NOS. INTO THE ARRAY A FROM A(1)
C       TO A(2N), AND SETS A1 EQUAL TO A.
C
C
      M=N*2
      DO 5  I=1,M
      A(I)=A1(I)
 5    CONTINUE
C
      RETURN
      END
      SUBROUTINE ERRTST(A,B,A1,B1,N)
      DIMENSION A(192),B(192),A1(192),B1(192)
C
C       THIS ROUTINE COMPUTES THE MAXIMUM ERROR BETWEEN THE VALUES OF
C       THE ORIGINAL ARRAY IN QUESTION AND THE ARRAY VALUES PRODUCED
C       BY CFT, IN ORDER TO CHECK THE ACCURACY OF CFT.
C
C
      ERROR=0.
      DO 5  I=1,N
      ERR=ABS(A(I)/FLOAT(N)-A1(I))
      IF(ERROR .LT. ERR) ERROR=ERR
      ERR=ABS(B(I)/FLOAT(N)-B1(I))
      IF(ERROR .LT. ERR) ERROR=ERR
 5    CONTINUE
      PRINT 100,ERROR
  100 FORMAT(1H0,5X,16HMAXIMUM ERROR = ,E18.10///)
C
      RETURN
      END
