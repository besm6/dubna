      FUNCTION PULSE (X)
      COMMON /C1/ FRQ, P
      COMMON /YN/ Y(9),N
      PN=1.
      DO 1 NY=1,N
 1    PN=PN*(X-Y(NY))
      PULSE=PN*SIN(FRQ*X+P)
      RETURN
      END
      SUBROUTINE ZSIN (FACTOR,NZ,ZERO)
      DIMENSION ZERO(NZ)
      DATA (PI=3.14159265358979)
      KS=1  $  AS=ABS(FACTOR)
      IF (FACTOR.LT.0.) KS=NZ
      DO 1 M=1,NZ
      AM=M-KS
 1    ZERO(M)=AM*PI*AS
      RETURN
      END
