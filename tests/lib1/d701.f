      PROGRAM D701
C        THIS PROGRAM TESTS ALL ENTRY POINTS OF THE SUBROUTINE PACKAGE F
C        (D701) BY CALLING EACH ROUTINE WITH RANDOM NUMBERS AND AFTER
C        INVERSION CHECK THE MAXIMAL ERROR ON ANY ELEMENT. FOR THE COMPL
C        ENTRYPOINTS, THE SPACING IS EQUAL TO ONE IN THE TEST.(THEY ARE
C        HOWEVER CALLED WITH THE SPACING EQUAL TO TWO FROM THE SINE AND
C        COSINE ROUTINES.)
      EXTERNAL I16,D16,I32,D32,I64,D64,I128,D128,S64,C64,S128,C128,
     ÐŸS256,C256,S512,C512
      COMMON A(257)
      READ 2,A
      PRINT 1,A
  1   FORMAT(1X,6E19.10)
  2   FORMAT(4E20.10)
      CALL T1(I16,D16,16)
      CALL T1(I32,D32,32)
      CALL T1(I64,D64,64)
      CALL T1(I128,D128,128)
      CALL T2(C64,64)
      CALL T2(C128,128)
      CALL T2(C256,256)
      CALL T2(C512,512)
      CALL T3(S64,64)
      CALL T3(S128,128)
      CALL T3(S256,256)
      CALL T3(S512,512)
      END
      SUBROUTINE T1(R1,R2,N)
      COMMON A(257)
      REAL B(256)
      CALL R1(A,A(129),1,B,B(129),1)
      CALL R2(B,B(129),1,B,B(129),1)
      FMAX = 0.
      DO 10 I=1,N
      R = ABS(A(I)-B(I)/N)
      S = ABS(A(128+I)-B(128+I)/N)
      IF(R.GT.FMAX) FMAX=R
   10 IF(S.GT.FMAX) FMAX=S
      PRINT 20,N,N,FMAX
   20 FORMAT(/5X,   1HI,I3,   5H  - D,I3,5X,E10.2)
      RETURN
      END
      SUBROUTINE T2(R1,N)
      COMMON A(257)
      REAL B(257)
      CALL R1(A,B)
      CALL R1(B,B)
      M = N/2+1
      FMAX = 0.
      DO 10 I=1,M
      R = ABS(A(I)-B(I)/N)
   10 IF(R.GT.FMAX) FMAX=R
      PRINT 20,N,N,FMAX
   20 FORMAT(/5X,   1HC,I3,   5H  - C,I3,5X,E10.2)
      RETURN
      END
      SUBROUTINE T3(R1,N)
      COMMON A(257)
      REAL B(257)
      CALL R1(A,B)
      CALL R1(B,B)
      M = N/2
      FMAX = 0.
      DO 10 I=2,M
      R = ABS(A(I)-B(I)/N)
   10 IF(R.GT.FMAX) FMAX=R
      PRINT 20,N,N,FMAX
   20 FORMAT(/5X,   1HS,I3,   5H  - S,I3,5X,E10.2)
      RETURN
      END
